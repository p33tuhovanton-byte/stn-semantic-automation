#!/bin/bash

### Массивы портов и названий сервисов для итерации автоматизма

SERVICES=("Python (FastAPI)" "Go-Service" "Rust-Service" "R-Service")  
PORTS=(8000 8010 8020 8030)

### 1\. Валидный пакет данных (Соответствует внешнему описанию JSON Schema)

VALID\_PAYLOAD='{  
"сумма": 850000,  
"субъект\_запроса": {"имя": "Светлана", "роль": "Бухгалтер", "грейд": "Middle"},  
"цель\_запроса": "Выплата зарплаты"  
}'

### 2\. Ошибочный пакет данных (Нарушено описание человека и описание цели)

INVALID\_PAYLOAD='{  
"сумма": 100000,  
"субъект\_запроса": {"имя": "Игорь", "роль": "Менеджер", "грейд": "Junior"},  
"цель\_запроса": "Покупка личных вещей"  
}'

echo "=========================================================================="  
echo "🤖 ЗАПУСК СКВОЗНОГО ТЕСТИРОВАНИЯ СЕМАНТИЧЕСКОГО АВТОМАТИЗМА (СТН)"  
echo "=========================================================================="

for i in "${!PORTS\[@\]}"; do  
SERVICE="{SERVICES\[i\]}"  
PORT="{PORTS\[i\]}"  
URL="http://127.0.0.1:$PORT/execute/transaction"

echo -e "\\n--------------------------------------------------------------------------"  
echo "🌐 Тестирование сервиса: SERVICE на порту PORT"  
echo "--------------------------------------------------------------------------"

\# Тест А: Отправка валидных данных (Кто и Зачем верны)  
echo "\[ТЕСТ А\] Отправка валидного контекста (Бухгалтер -> Выплата зарплаты):"  
RESPONSE\_A=(curl -s -X POST "URL"  
\-H "Content-Type: application/json"  
\-d "$VALID\_PAYLOAD")  
echo "Ответ сервера: $RESPONSE\_A"

echo ""

\# Тест Б: Отправка ошибочных данных (Защита должна сработать автоматически)  
echo "\[ТЕСТ Б\] Отправка некорректного контекста (Менеджер -> Покупка личных вещей):"  
RESPONSE\_B=(curl -s -X POST "URL"  
\-H "Content-Type: application/json"  
\-d "$INVALID\_PAYLOAD")  
echo "Ответ сервера: $RESPONSE\_B"  
done

echo -e "\\n=========================================================================="  
echo "✅ Все тесты отправлены. Проверьте логи терминалов запущенных сервисов."  
echo "=========================================================================="