#!/bin/bash

# Событие 1: Нормальный проход (Дверь открыта -> вошли люди -> закрыта)
PAYLOAD_ROOM_ACCESS='{
  "наименование": "Главный вход",
  "состояние_двери": "открыта",
  "количество_людей": 3,
  "статус_охраны": "выключен"
}'

# Событие 2: Тревога (Дверь открыта в режиме охраны, зафиксирован несанкционированный вход)
PAYLOAD_SECURITY_ALERT='{
  "наименование": "Запасной выход",
  "состояние_двери": "открыта",
  "количество_людей": 1,
  "статус_охраны": "активен"
}'

# Событие 3: Штатное состояние закрытого объекта
PAYLOAD_CLOSED_STATE='{
  "наименование": "Главный вход",
  "состояние_двери": "закрыта",
  "количество_людей": 0,
  "статус_охраны": "активен"
}'

echo "=========================================================================="
echo "🤖 ТЕСТИРОВАНИЕ СТН: КОНТРОЛЬ ДОСТУПА И УМНЫЙ ДОМ"
echo "=========================================================================="

# Тест А: Фиксация обычного входа сотрудников
echo -e "\n=== [ТЕСТ А] Регистрация прохода: Дверь открыта, вход 3 человек ==="
curl -X POST -H "Content-Type: application/json" -d "$PAYLOAD_ROOM_ACCESS" http://localhost:8000/validate || echo "Сервер не ответил"

# Тест Б: Мониторинг закрытого состояния
echo -e "\n=== [ТЕСТ Б] Мониторинг: Объект закрыт, на охране ==="
curl -X POST -H "Content-Type: application/json" -d "$PAYLOAD_CLOSED_STATE" http://localhost:8000/validate || echo "Сервер не ответил"

# Тест В: Симуляция критической ситуации (Нарушение сути безопасности)
echo -e "\n=== [ТЕСТ В] Тревога: Дверь открыта в режиме охраны! ==="
curl -X POST -H "Content-Type: application/json" -d "$PAYLOAD_SECURITY_ALERT" http://localhost:8000/validate || echo "Сервер не ответил"

echo -e "\n=========================================================================="
echo "✅ СЕРИЯ ТЕСТОВ ЗАВЕРШЕНА"
echo "=========================================================================="
